#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	x;
	char	*y;

	i = 0;
	x = (char)c;
	y = (char*)s;
	while (y[i] != '\0')
	{
		if (x == y[i])
			return (&y[i]);
		i++;
	}
	if (x == '\0' && y[i] == '\0')
		return (&y[i]);
	return (NULL);
}
