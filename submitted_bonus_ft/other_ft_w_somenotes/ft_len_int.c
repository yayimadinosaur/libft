#include "libft.h"

int		ft_len_int(long nb)
{
	int	lent_int;

	len_int = 0;
	if (nb < 0)
	{
		len_int++;
		nb = nb * -1;
	}
	if (nb == 0)
	{
		len_int++;
	}
	else
	{
		while (nb > 0)
		{
			len_int++;
			nb = nb / 10;
		}
	}
	return (len_int);
}
