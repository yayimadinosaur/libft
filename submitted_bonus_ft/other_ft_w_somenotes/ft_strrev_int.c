#include "libft.h"

char	*ft_strrev_int(char *str)
{
	int		i;
	int		j;
	char	*x;

	i = 0;
	j = 0;
	while (str[i] != '\0')
		i++;
	i = i - 1;
	if (str[j] == '-')
		j++;
	while (j < i)
	{
		x = str[j];
		str[j] = str[i];
		str[i] = x;
		i--;
		j++;
	}
	return (str);
}
