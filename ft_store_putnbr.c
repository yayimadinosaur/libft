#include "libft.h"

char	*ft_store_putnbr(char *str, long n)
{
	int	i;

	i = 0;
	if (n < 0)
	{
		str[i] = '-';
		i++;
		n = -n;
	}
	else if (n == 0)
	{
		str[i] = 48;
		i++;
	}
	while (n > 0)
	{
		str[i] = (n % 10) + 48;
		n = n / 10;
		i++;
	}
	str[i] = '\0';
	return (str);
}
