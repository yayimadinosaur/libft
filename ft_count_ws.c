#include "libft.h"

int		ft_count_ws(char *str)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	if (!str)
		return (0);
	while (str[i] != '\0')
	{
		if (str[i] == ' ' || str[i] == '\t' || str[i] == '\n')
		{
			j++;
			i++;
		}
		else if (str[i] == ' ' || str[i] == '\t' | str[i] == '\n')
			i++;
	}
	return (j);
}
