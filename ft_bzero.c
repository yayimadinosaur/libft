#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*x;

	x = (unsigned char*)s;
	i = 0;
	if (n == 0)
		return ;
	else
		while (n > 0)
		{
			x[i] = '\0';
			i++;
			n--;
		}
}
